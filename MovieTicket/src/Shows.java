
public class Shows {
	
	private int id;
	private String City;
	private String Theater;
	private String id_city;
	private String Times;
	private String tittle;
	private String seats;
	private String id_city_theater_time;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getCity() {
		return City;
	}
	public void setCity(String city) {
		City = city;
	}
	public String getTheater() {
		return Theater;
	}
	public void setTheater(String theater) {
		Theater = theater;
	}
	public String getId_city() {
		return id_city;
	}
	public void setId_city(String id_city) {
		this.id_city = id_city;
	}
	public String getTimes() {
		return Times;
	}
	public void setTimes(String times) {
		Times = times;
	}
	public String getTittle() {
		return tittle;
	}
	public void setTittle(String tittle) {
		this.tittle = tittle;
	}
	public String getSeats() {
		return seats;
	}
	public void setSeats(String seats) {
		this.seats = seats;
	}
	public String getId_city_theater_time() {
		return id_city_theater_time;
	}
	public void setId_city_theater_time(String id_city_theater_time) {
		this.id_city_theater_time = id_city_theater_time;
	}
	
	
	
	

}
